Input: 
Weather table:
+----+------------+-------------+
| id | recordDate | temperature |
+----+------------+-------------+
| 1  | 2015-01-01 | 10          |
| 2  | 2015-01-02 | 25          |
| 3  | 2015-01-03 | 20          |
| 4  | 2015-01-04 | 30          |
+----+------------+-------------+
Output: 
+----+
| id |
+----+
| 2  |
| 4  |
+----+



select w1.id
from Weather w1
where temperature > (
    select temperature
    from Weather w2
    where w2.recordDate=DATE_SUB(w1.recordDate,INTERVAL 1 DAY)
);